#include <iostream>
using namespace std;

int main(int argc, char const *argv[])
{
	int a, b;
	cin >> a;
	cin >> b;

	cout << a + b << endl;
	cout << a - b << endl;
	cout << a * b << endl;
	cout << a / b << endl;
	cout << a % b << endl;
	return 0;
}
